package io.github.algorys.agshmne.character;

public class Attribute {
	private int FOR;
	private int DEX;
	private int CON;
	private int INT;
	private int CHA;

    public Attribute () {
    }

	public int getFOR() {
		return FOR;
	}

	public void setFOR(int fOR) {
		FOR = fOR;
	}

	public int getDEX() {
		return DEX;
	}

	public void setDEX(int dEX) {
		DEX = dEX;
	}

	public int getCON() {
		return CON;
	}

	public void setCON(int cON) {
		CON = cON;
	}

	public int getINT() {
		return INT;
	}

	public void setINT(int iNT) {
		INT = iNT;
	}

	public int getCHA() {
		return CHA;
	}

	public void setCHA(int cHA) {
		CHA = cHA;
	}
    
    
}
